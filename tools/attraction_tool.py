def get_attractions(city: str) -> list[dict]:
    """Gets popular tourist attractions and landmarks for a specified city.

    Args:
        city (str): The name of the city (e.g., "Taipei", "Taichung", "Yilan", "Kaohsiung").

    Returns:
        list[dict]: A list of dictionaries, where each dictionary represents an attraction.
                    Returns an empty list if the city is unsupported.
    """
    if not isinstance(city, str):
        return []

    # Normalize input city name by stripping whitespace and capitalizing
    normalized_city = city.strip().capitalize()
    
    # Safely fetch the attractions list, default to empty list if unsupported
    return MOCK_ATTRACTIONS_DB.get(normalized_city, [])

def filter_by_type(city: str, attraction_type: str) -> list[dict]:
    """Filters attractions in a city by their type (Indoor/Outdoor).

    Args:
        city (str): The name of the city.
        attraction_type (str): The type to filter by ("Indoor" or "Outdoor").

    Returns:
        list[dict]: A list of filtered attraction dictionaries.
    """
    attractions = get_attractions(city)
    if not isinstance(attraction_type, str):
        return []
    
    target_type = attraction_type.strip().lower()
    return [attr for attr in attractions if attr.get("type", "").lower() == target_type]

def filter_by_category(city: str, category: str) -> list[dict]:
    """Filters attractions in a city by their category.

    Args:
        city (str): The name of the city.
        category (str): The category to filter by (e.g., "Nature", "Museum", "Landmark").

    Returns:
        list[dict]: A list of filtered attraction dictionaries.
    """
    attractions = get_attractions(city)
    if not isinstance(category, str):
        return []
    
    target_category = category.strip().lower()
    return [attr for attr in attractions if attr.get("category", "").lower() == target_category]

